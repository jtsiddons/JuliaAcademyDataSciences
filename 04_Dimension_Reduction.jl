md"""
We will be using the `mtcars` dataset from the `VegaDatasets` package. This is about car specs for over 400 car models.

Let's load the dataset. To demonstrate a different technique, I will `pipe` the dataset into a `DataFrame` using the `pipe` object: `|>`. This isn't as fully functioned as `%>%` in R `dplyr`, but it is useful to know about! There are Julia packages that enhance the abilities of `|>`.
"""

# ╔═╡ 906ae0e6-ae1b-4dc6-aa34-120edddf94c1
car_df = VegaDatasets.dataset("cars") |> DataFrame

# ╔═╡ 22eb44b5-f9f9-425d-be5d-84a6ce5556ea
dropmissing!(car_df)

# ╔═╡ 5f35086b-6f5e-425f-a9bd-620c6d19239f
names(car_df)

# ╔═╡ 34e6be49-322c-41c4-b8d7-61024bf35a60
 describe(car_df)

# ╔═╡ abc6f39f-3b3b-4741-9797-660208b3d715
md"""
### PCA - Principal Component Analysis

PCA projects data onto a few principal components. In this case the principal components are a direction that maximizes the variance of the projected data. The $i$-th principal component can be taken as a direction orthogonal to the first $i-1$ principal components that maximizes the variance of the projected data.

This means that the principal components are the eigen-vectors of the covariance matrix of the data.

We can use the PCA in the `MultivariateStats.jl` package.

First, we need to centre and scale the data. It needs to be a matrix.
"""

# ╔═╡ ae227efd-9bd5-4a04-8935-285d982e7c58
car_M = Matrix(car_df[:,2:7])

# ╔═╡ 14f195de-983b-4d5b-81f3-10b88a400877
md"""
Subtract column means, and divide by column standard deviations
"""

# ╔═╡ 87f172dd-6888-4ce5-8306-ff9fb3209460
car_M_cs = (car_M .- mean(car_M, dims=1)) ./ std(car_M, dims=1)

# ╔═╡ b060d3e1-47a0-4e6f-b859-b6852856507d
md"""
PCA expects that the columns are observations, so we need to use the transpose of the above matrix.
"""

# ╔═╡ d7468d7e-15cd-442c-9c95-0af3970e5b6b
car_PCA = fit(PCA, car_M_cs', maxoutdim=2)

# ╔═╡ e3e83723-a99d-41cf-9a1a-1cb7010c1ee7
md"""
We obtain the projection matrix by calling `projection`.
"""

# ╔═╡ 8e49dd77-015a-4c21-bb2c-03db0d17e467
car_PCA_Proj = projection(car_PCA)

# ╔═╡ 5c3f132f-86ec-4a4f-a108-0a1b14df20d9
md"""
Let's look at the actual method here.

We can see that the 2 principal components are the last two columns of the eigenvector matrix of the correlation matrix. We apply a permutation to re-order it in terms of decreasing eigenvalue.

To get the percentage of variance explained we look at the cumulative sum of the corresponding eigenvalues.
"""

# ╔═╡ 227d3b45-77fd-4d29-bfc6-76dd3bec3cf6
begin
	car_cor = cor(car_M_cs)
	permute_evals = 6:-1:1;
	car_cor_eval = eigvals(car_cor)[permute_evals]
	car_cor_evec = eigvecs(car_cor)[:,permute_evals]
end	

# ╔═╡ d69374b3-9f32-495a-9f9e-94eb5d243d11
car_cor_eval

# ╔═╡ db86b825-7058-48b9-8064-2a3c3374baea
md"""
Let's do a scree plot.
"""

# ╔═╡ ed934332-a56d-4dcf-b29c-a36b95246f76
begin
	car_perc_explained = round(cumsum(car_cor_eval)[2]/0.06,digits=2) #divide by 0.06 = 6/100 - we have 6 eigenvalues & we want percentages.
	car_screeplt = Figure()
	screeax1 = Axis(car_screeplt[1,1])
	lines!(screeax1,car_cor_eval)
	screeax2 = Axis(car_screeplt[1,2],title="Variance Explained", ylabel="Percentage")
	lines!(screeax2,cumsum(car_cor_eval)./0.06)
	hlines!(screeax2,[cumsum(car_cor_eval)[2]/0.06],color=:red)
	car_screeplt
end

# ╔═╡ 222cb1f6-755f-4766-8c04-c740e4643f05
md"""
We can see that the first two principal components explain approximately $car_perc_explained% of the variance in the data.

We can now transform the data. To do this we multiply the scaled data matrix by the principal components. Or we can use `transform`.
"""

# ╔═╡ 28408fef-3bea-4b4a-b547-abc030f41145
begin
	car_princ_comps = car_cor_evec[:,1:2]
	car_transformed = car_M_cs * car_princ_comps
end

# ╔═╡ 1fdadc3d-7b6e-474a-8f76-caa8fbe6b444
md"""
We can see that the result from direct computation is very close to the result from `MultivariateStats.jl`.
"""

# ╔═╡ 2466fe0d-c33e-4fc2-87d2-bad53f8ff9a5
norm(car_transformed' - MultivariateStats.transform(car_PCA, car_M_cs'))

# ╔═╡ 0f9f0374-85d6-414b-9d2c-e4024558e3c0
md"""
To approximately reconstruct our original data, we multiply the transform by the transposed principal components.

We could also use `reconstruct`
"""

# ╔═╡ cdef8013-54a3-4823-9a11-4b7b0d6d3379
car_reconstructed = car_transformed * car_princ_comps'

# ╔═╡ 11f3a823-4173-4a13-8fa3-5904efa2b80b
norm(car_M_cs - car_reconstructed)

# ╔═╡ acc22d63-8e7a-41e1-8b81-4b3104f0d033
[
	norm(car_M_cs - car_reconstructed),
	norm(car_M_cs' - reconstruct(car_PCA,MultivariateStats.transform(car_PCA, car_M_cs'))),
	norm(car_M_cs - (car_M_cs * car_cor_evec) * car_cor_evec')		
]

# ╔═╡ d4b79ebe-5b34-409d-a90d-cdb26d996f19
md"""
As we can see, the method gives the same result if we use direct computation or `reconstruct`. 

If we use all 6 principal components we basically get the data back. We should expect this.

Let's now do a plot of this PCA.
"""

# ╔═╡ 91da0128-8f61-4826-9af3-28cef500d612
begin
	car_PCA_plt = Figure()
	xlab = "Component 1: Variance Explained = "*string(round(car_cor_eval[1]/0.06, digits=2))*"%"
	ylab = "Component 2: Variance Explained = "*string(round(car_cor_eval[2]/0.06, digits=2))*"%"
	PCAax1 = Axis(car_PCA_plt[1,1], title="PCA", xlabel=xlab, ylabel=ylab)
	xs = zeros(6); ys = zeros(6)
	us = car_princ_comps[:,1]; vs= car_princ_comps[:,2]
	scatter!(PCAax1, car_transformed[car_df.Origin.=="USA",1], car_transformed[car_df.Origin.=="USA",2],label="USA")
	scatter!(PCAax1, car_transformed[car_df.Origin.=="Europe",1], car_transformed[car_df.Origin.=="Europe",2],label="Europe")
	scatter!(PCAax1, car_transformed[car_df.Origin.=="Japan",1], car_transformed[car_df.Origin.=="Japan",2],label="Japan")
	arrows!(PCAax1,xs, ys, us.*3, vs.*3, color=:red)
	textLocs = [(us[i].*3, vs[i].*3) for i=1:6]
	text!(PCAax1,names(car_df)[2:7],position=textLocs)
	axislegend()
	car_PCA_plt
end

# ╔═╡ faa0a777-3c58-4167-918d-48d496cb7b0e
md"""
We can see some really cool results here. Notably that American cars are associated with low MPG, are heavier and more powerful. European and Japanese cars are more efficient.

We find 3 clusters - with US cars dominating 2 of them. 

Note that in the above plot I scaled the principal components $\times$ 3 to fit them nicely on the plot.

### t-SNE (t-distributed Stochastic Neighbour Embedding)

The method I use here is a bit cheaty as we're using `ScikitLearn.jl` which basically uses the python package `Scikit-learn`.
"""

# ╔═╡ caecdcb8-10db-406c-8304-9891f636addf
begin
	car_unique_id = labelmap(car_df.Origin) 
	car_unique_id = labelencode(car_unique_id, car_df.Origin) # Assign unique ID based on origin - for colouring
	@sk_import manifold : TSNE # Import from Scikit-Learn
	tfn = TSNE(n_components=2) # Two components
	car_tfn = tfn.fit_transform(car_M_cs); # Apply transformation
	
	## Plot the result.
	tfn_plot = Figure()
	tfnax1 = Axis(tfn_plot[1,1])
	scatter!(tfnax1, car_tfn[:,1], car_tfn[:,2], color=car_unique_id)
	tfn_plot
end	

# ╔═╡ b2e93932-dea2-429c-9d4a-bdcc044cf505
md"""
We observe the same pattern as with PCA - 3 clusters. They are much more well defined here though.

### UMAP (Uniform Manifold Approximation and Projection)

We use the `UMAP.jl` package. Unfortunately, I get a `Distributed.ProcessExitedException(6)` error with this package in `Pluto.jl`. I have disabled the cell.
"""

# ╔═╡ 8a8086ca-5c09-4aa7-8af6-dd23b48c13e3
begin
	car_samples_cor = cor(car_M_cs,car_M_cs,dims=2)
	car_umap = umap(car_samples_cor, 2)
end

