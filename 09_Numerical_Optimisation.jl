### Problem 3: Diet Optimisation

In this problem we are given a number of constraints of the food we eat - the number of calories, protein, fat and sodium limits we can consume.

We want to minimise spending whilst maintianing the limits of our diet.

We will use `JuMP.jl` to solve this, there is a specific example on their [documentation](https://jump.dev/JuMP.jl/v0.21.10/tutorials/Mixed-integer%20linear%20programs/diet/) that investigates this specific problem. The example we follow here is identical to the one shown in the package documentation.

The original notebook loads the required packages now, I've added them to the start of this notebook.

We will first build a `JuMP` container to store our constraints. These are the minimum and maximum daily limits.
"""

# ╔═╡ 28a0b4fd-74e3-47a9-a551-20cecabfec31
category_data = JuMP.Containers.DenseAxisArray(
	[1800 	2200;
	 91 	Inf;
	 0 		65;
	 0 		1779],
	["calories", "protein", "fat", "sodium"],
	["min", "max"])

# ╔═╡ d2ff95b7-125b-4aaa-9581-a9539c61f131
md"""
This is kind of like a matrix indexed by two vectors of strings rather than integers. Almost like a mulit-key dictionary.
"""

# ╔═╡ c7898841-bc2c-4313-8a88-22bd6d8b694b
category_data["calories", "max"]

# ╔═╡ 107c5a6e-e652-4f18-9cf9-b68087db4787
md"""
Now we encode some information about food data.
"""

# ╔═╡ 9c530df7-c037-4ce0-8bda-8a431f01ca62
begin
	foods = ["hamburger", "chicken", "hot dog", "fries", "macaroni", "pizza","salad", "milk", "ice cream"];
	categories = ["calories", "protein", "fat", "sodium"];
	cost = JuMP.Containers.DenseAxisArray(
		[2.49, 2.89, 1.50, 1.89, 2.09, 1.99, 2.49, 0.89, 1.59],
		foods)
end

# ╔═╡ 81a99b15-93f9-484b-963e-b1cb6f686b94
md"""
The above cell has worked - even though it shows an error. It may be a quirk of how Pluto displays results.
"""

# ╔═╡ 5e5f720d-6066-4d5e-be2b-ee1b6f9d11f3
cost["fries"]

# ╔═╡ fb658aa0-d4af-4fc8-b3da-979d5f94cf1b
food_data = JuMP.Containers.DenseAxisArray(
    [410 24 26 730;
     420 32 10 1190;
     560 20 32 1800;
     380  4 19 270;
     320 12 10 930;
     320 15 12 820;
     320 31 12 1230;
     100  8 2.5 125;
     330  8 10 180], 
    foods, categories 
    )

# ╔═╡ 03ddba06-90d3-4ee5-81ff-28b423edb491
md"""
Now we build our `JuMP` model.
"""

# ╔═╡ 6057de06-e743-4c54-8b29-028d63367d10
diet_model = Model(GLPK.Optimizer)

# ╔═╡ ccd43a92-831f-49b4-8858-359a488eeb94
# Variables
@variables(diet_model, begin
		# nutrition info
		category_data[c,"min"] <= nutrition[c=categories] <= category_data[c,"max"]
		# Which food to buy
		buy[foods] >= 0
	end)

# ╔═╡ 2664992d-afb8-4592-9dee-fbd9385d8259
# Objective
@objective(diet_model, Min, sum(cost[f] * buy[f] for f in foods))

# ╔═╡ 2f9dbff0-dc15-45bc-8411-de181fe51933
# Nutritional Constraint - the amount of food is constrained
# by the min and max allowances for each category
@constraint(diet_model, [c in categories],
	sum(food_data[f,c] * buy[f] for f in foods) == nutrition[c]
	)

# ╔═╡ a47c6ff8-b0b6-4afd-a4a0-3cd973c005ef
md"""
Now we solve the problem
"""

# ╔═╡ e8a7b7c7-5fd0-4250-b2ca-f8226a8f8db7
JuMP.optimize!(diet_model)

# ╔═╡ e35b4e32-ac46-4139-aa29-21ce580fc386
md"""
Check the results are optima;
"""

# ╔═╡ f3b1d1e7-f3fc-4680-808e-7913dfca45bf
term_status = JuMP.termination_status(diet_model)

# ╔═╡ f133b953-b026-461e-a27a-d6a6440ce648
[
	term_status == MOI.OPTIMAL;
	JuMP.primal_status(diet_model) == MOI.FEASIBLE_POINT
	]

# ╔═╡ 12e4d969-336f-499e-9632-544e0a21d2e6
JuMP.objective_value(diet_model) ≈ 11.8288; atol=1e-4

