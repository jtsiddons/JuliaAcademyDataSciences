### Problem 2: Image Recovery

We have been given an altered image. The goal is to recover the original pixels by solving an optimisation problem. We first load the figure.
"""

# ╔═╡ 739191be-7551-4060-ab08-371ad3d01e52
orig_img = load("data/khiam-small.jpg")

# ╔═╡ 9ee509f5-971f-464a-b947-0f5a81ee90af
md"""
Let's modify some of the pixels. We convert to grayscale.
"""

# ╔═╡ 1829e66e-27b0-40d7-b60b-a42e89ab8451
begin
	mod_img = copy(orig_img)
	p = prod(size(mod_img))
	missingids = rand(1:p, 400) # randomly select 400 pixels
	mod_img[missingids] .= RGBX{N0f8}(0.0,0.0,0.0)
	Gray.(mod_img)
end

# ╔═╡ 32e2fcd6-a97c-43ed-8161-95924bd29ce0
md"""
Given our image, the goal is now to complete the matrix. We use a common approach by Candes and Tao (I think the link is [this](https://ieeexplore.ieee.org/stamp/stamp.jsp?arnumber=5452187). The goal is to create a new matrix `X̂` where we are minimising the `nuclearnorm` of `X̂` (the sum of singular values of `X̂`) and such that the entries that are already in our image remain the same in `X̂`. We can again use `Convex.jl`.
"""

# ╔═╡ b7ec7ae8-30a4-4b53-8709-198b0693c8fa
begin
	Ŷ = Float64.(Gray.(mod_img));
	
	correctids = findall(Ŷ[:].!=0);
	X̂ = Convex.Variable(size(Ŷ));
	problem_img = minimize(nuclearnorm(X̂))
	problem_img.constraints += X̂[correctids] == Ŷ[correctids]
	problem_img
end

# ╔═╡ 09a8fb6d-95f8-451b-9304-82d0be6aaa39
solve!(problem_img, SCS.Optimizer(eps=1e-3, alpha=1.5))

# ╔═╡ a1f76f97-e5d0-4f45-a0f1-0675380d1317
colorview(Gray, X̂.value)

# ╔═╡ 092790a0-fb7a-49f9-a39e-efbb21e83608
[
	norm(float.(Gray.(orig_img)) - X̂.value),
	norm(-X̂.value)
]

