	for i in 1:3
		str_invest_i = string(round(x.value[i]*1000, digits=2)) * "USD in " * names(stock_prices)[i]
		str_invest_i = i == 3 ? "and "*str_invest_i*"." : str_invest_i*", "
		str_invest *= str_invest_i
	end
end

