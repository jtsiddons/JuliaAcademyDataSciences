# ╔═╡ 4857edaf-e3a4-4033-af2a-63aa3050835f
md"""
#### Weekly returns

Compute a matrix of weekly returns, which we compute as `Return[i,t] = (price[i,t] - price[i,t-1])/price[i,t-1]`. This is the return of stock `i` from week `t`.
"""

# ╔═╡ 8ca769eb-55e2-48bb-a54e-6e4b4a2ce1df
weekly_return = (
	Matrix(stock_prices)[2:end,:] - Matrix(stock_prices)[1:end-1,:])./Matrix(stock_prices)[1:end-1,:]

# ╔═╡ a20ef483-d4b5-46d6-9524-7179c83f591a
md"""
If `x=[x₁, x₂, x₃]` is the total number of dollars spent investing in each company. The return on investment will be `dot(r, x)` where `r=[r₁, r₂, r₃]` is the return from each company. Here `r` is the random variable we want to model. We want to model in terms of _expected values_. The expected value `E(dot(r,x))` will be `E(dot(mean(weekly_return, dims=1), x)`. If we want a return of 10% or more we require `dot(r,x) ≥ 0.1`.

#### Risk Matrix

We compute the risk matrix. The derivation for it can be read [here](https://www.kdnuggets.com/2019/06/optimization-python-money-risk.html). Simply, the risk matrix is the covariance matrix of the return prices: `weekly_return`.
"""

# ╔═╡ 6b3287d5-e473-435f-abb2-8d66d42c557f
Aᵣᵢₛₖ = cov(weekly_return)

# ╔═╡ 50fd11a5-74b9-4f03-89b5-5b205118977d
md"""
This matrix is positive definite. We compute our average return.
"""

# ╔═╡ b45dda4a-6275-49f5-a6e4-18d396071d6f
isposdef(Aᵣᵢₛₖ)

# ╔═╡ f20a3983-1f70-49d3-9bf6-22cd7110a1b1
r_av_return = mean(weekly_return, dims=1)[:]

# ╔═╡ 93e5bba2-80a5-402a-92da-69b80f4ec886
md"""
#### An example problem

Let's say that someone gives you $1000 and asks you to invest in these three companies such that you get a return of 2%. 

The goal is to minimise the risk. This is computed my minimising

$$\underline{x}^T \times \hat{A}_{risk} \times \underline{x}$$

where $\hat{A|_{risk}$ is the risk matrix. We must also satisfy the following constraints

* $\sum_i x_i = 1$
* $\underline{r} \cdot \underline{x} \geq 0.02$
* $x_i \geq 0$

This is a convex problem and we can use `Convex.jl` to solve it.
"""

# ╔═╡ 4e2d71b8-df49-40ec-a79a-ea4f30b5d96b
begin
	x = Variable(length(r_av_return));
	
	invest_constraints = [sum(x)==1; r_av_return'*x>=0.02; x.>0]
	# Note that the constraints are separated by ";" not ","
	problem = minimize(x'*Aᵣᵢₛₖ*x, invest_constraints)
end

# ╔═╡ b6de13f9-84d2-4662-95b2-5723767e6664
md"""
Note that the problem above is not DCP compliant. We get an warning in our `Julia` terminal, and have `Convex.NotDcp` in our `minimize` output. We need to pass a DCP compliant problem (Disciplined Convex Programming). We can learn about the rules [here](http://cvxr.com/cvx/doc/dcp.html).
"""

# ╔═╡ 4c20d493-c431-4be5-ad8a-ebd384836629
problem_DCP = minimize(Convex.quadform(x, Aᵣᵢₛₖ), invest_constraints) 

# ╔═╡ 5209c59f-d9e9-4422-92ca-b7308350ce52
md"""
We now have `convex; positive` in our output.

We solve using `solve!` and require an optimiser, we use the `Optimizer` from the `SCS` package.
"""

# ╔═╡ 69000532-c2b7-4967-9742-88e8c38d2361
solve!(problem_DCP, SCS.Optimizer)

# ╔═╡ 9fcc98e6-6a5d-4e56-9898-cefe1e6f8025
x

# ╔═╡ d34ffa63-b781-4eea-a58c-be8d514ec547
[
	sum(x.value);
	r_av_return' * x.value
]

